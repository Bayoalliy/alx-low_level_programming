/**
 * op_add - add two integers.
 * @a: first number.
 * @b: second number.
 *Return: sum of two integers.
 */


int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - find the difference of two imtegers.
 * @a: first integer.
 * @b: second integer.
 * Return: difference of two integers.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers.
 * @a: first integer.
 * @b: second integer.
 *Return: multiplication of two integers.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide two numbers.
 * @a: first integer.
 * @b: second integer.
 * Return: division of the two numbers.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - finds the modulus of a / b.
 * @a: first integer.
 * @b: second integer.
 * Return: modula.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
